function getLang() {
  return localStorage.getItem(LANG_KEY) || "fr";
}
function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
}
function t(key) {
  const lang = getLang();
  const dict = I18N[lang] || I18N.fr;
  return dict[key] !== undefined ? dict[key] : (I18N.fr[key] !== undefined ? I18N.fr[key] : key);
}
